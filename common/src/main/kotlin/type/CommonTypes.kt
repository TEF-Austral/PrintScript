package type

enum class CommonTypes {
    // New Token Types
    DELIMITERS, // ( ) { } , . ; : ?
    OPERATORS, // + - * / %
    ASSIGNMENT, // =
    COMPARISON, // == != < > <= >=
    LOGICAL_OPERATORS, // && ||
    CONDITIONALS, // if else switch case
    LOOPS, // for while do
    DATA_TYPES, // NUMBER STRING
    STRING_LITERAL, // "Hello" 'World'
    NUMBER_LITERAL, // 123 45.67
    IDENTIFIER, // variable names, function names
    RETURN, // return
    FUNCTION, // function
    DECLARATION, // let const var
    PRINT, // println
}
